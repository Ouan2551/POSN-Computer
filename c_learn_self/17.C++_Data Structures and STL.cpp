#include <bits/stdc++.h>
using namespace std;
int main()
{
    // Data structures => used to store and organize data
    // An array is an example of a data structure
    // which allows multiple elements to be stored in a single variable.

    // C++ STL => library that consist of different data structures and algorithms
    // Danger => using right Data structures to make program run faster

    //Data Structure	    Description
    // Vector	            Stores elements like an array but can dynamically change in size
    //                      Adding and removing of elements are usually done at the end
    //                      Elements can be accessed by index.

    // List	                Stores elements sequentially, where each element is connected to the next
    //                      Adding and removing of elements can be done at both ends
    //                      Not accessible by index.

    // Stack	            Stores elements in a specific order, called LIFO (Last In, First Out)
    //                      where elements can only be added and removed from the top
    //                      Not accessible by index.

    // Queue	            Stores elements in a specific order, called FIFO (First In, First Out)
    //                      where elements are added at the end and removed from the front
    //                      Not accessible by index.

    // Deque	            Stores elements in a double-ended queue, where elements can be added
    //                      and removed from both ends
    //                      Elements can be accessed by index.
    
    // Set	                Stores unique elements. Not accessible by index.
    // Map	                Stores elements in "key/value" pairs. Accessible by keys (not by index).

    //Key Concepts of the STL
    // Containers => are data structures that provides a way to store data, like vectors, lists, etc.
    // Iterators  => are objects used to access elements of a data structure.
    // Algorithms => include functions, like sort() and find(), that perform operations on data structures through iterators.

    return 0;
}